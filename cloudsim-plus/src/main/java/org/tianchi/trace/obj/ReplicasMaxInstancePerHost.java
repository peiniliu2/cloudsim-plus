package org.tianchi.trace.obj;

public class ReplicasMaxInstancePerHost {
	
	private int replicas;
	
	private String restrain;
	
	private int maxInstancePerHost;

	public int getReplicas() {
		return replicas;
	}

	public void setReplicas(int replicas) {
		this.replicas = replicas;
	}

	public String getRestrain() {
		return restrain;
	}

	public void setRestrain(String restrain) {
		this.restrain = restrain;
	}

	public int getMaxInstancePerHost() {
		return maxInstancePerHost;
	}

	public void setMaxInstancePerHost(int maxInstancePerHost) {
		this.maxInstancePerHost = maxInstancePerHost;
	}
	

}
