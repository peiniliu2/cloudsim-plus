package org.tianchi.trace.obj;

public class GroupMaxInstancePerHost {

	private String group;
	
	private boolean compactness;
	
	private int maxInstancePerHost;

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public boolean isCompactness() {
		return compactness;
	}

	public void setCompactness(boolean compactness) {
		this.compactness = compactness;
	}

	public int getMaxInstancePerHost() {
		return maxInstancePerHost;
	}

	public void setMaxInstancePerHost(int maxInstancePerHost) {
		this.maxInstancePerHost = maxInstancePerHost;
	}
}
